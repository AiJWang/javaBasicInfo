package MsbStudy.BasicInfo;
/**
 * 位运算
 * 6>>2: 6 右移2位置
 * 00000000 00000000 00000000 000000110
 *   00000000 00000000 00000000 000000110
 *  -----------------------------------------
 * 00000000 00000000 00000000 000000001
 * 所以最后结果是1，所以右移是除法，6/2/2=1
 * 6<<2  6左移2位
 *   00000000 00000000 00000000 000000110
 * 00000000 00000000 00000000 00000011000
 *  -----------------------------------------
 * 00000000 00000000 00000000 000011000
 * 结果是24，所以左移是乘法，6*2*2=24
 *
 * -6>>2 -6右移2位
 *  -6的二进制，就是6的二进制，取反，加1
 *  00000000 00000000 00000000 000000110
 *  取反
 *  11111111 11111111 11111111 11111001
 *  加一
 *                                    1
 *  ----------------------------------------
 *  11111111 11111111 11111111 11111010
 *    11111111 11111111 11111111 11111010
 *    -------------------------------------右移2位，补位的是符号位，所以补1
 *  11111111 11111111 11111111 11111110
 *                                    1
 *  ---------------------------------------  -1
 *  11111111 11111111 11111111 11111101
 *  ---------------------------------------   取反
 *  00000000 00000000 00000000 00000010=====-2
 *
 *  6&3  按位与
 *  00000000 00000000 00000000 000000110
 *  00000000 00000000 00000000 000000011
 *  -------------------------------------------
 *  00000000 00000000 00000000 000000010====2
 *  按位或就不写了，看下异或，相同为0，不同为1
 *  6^3  6异或3
 *  00000000 00000000 00000000 000000110
 *  00000000 00000000 00000000 000000011
 *  -------------------------------------------
 *  00000000 00000000 00000000 000000101====5
 *
 *  ~6  取反 =-7
 *  00000000 00000000 00000000 000000110
 *  ------------------------------------
 *  11111111 11111111 11111111 11111001   一看就是个负数，转化10进制. 先 -1，取反
 *                                    1
 * ---------------------------------------
 *  11111111 11111111 11111111 11111000 取反
 *  ---------------------------------------
 *  00000000 00000000 00000000 00000111   ====先化成10进制7，再价格负号
 *
 *  byte取值范围，因为是1个字节， 所以最大值是
 *  011111111==127
 *  负数
 *  10000000   
 *  ---------------------- -1
 *  01111111
 *  ---------------------  取反
 *  10000000    =-128
 *

 * */
